    <p><font size=6 style = "font-family:Verdana; background-color:FFF702 " >¿POR QUE VOTAR?</font></p>
      <p style = "font-familyVerdanas; text-align: justify; background-color:212EF9; color:white " >
      El voto es una condición necesaria para que un sistema político sea democrático. Hay gobiernos autoritarios en los cuales las restricciones para la presentación de las candidaturas alcanzan un grado que impide considerarlos democráticos.
    </p>
    <p style = "font-family:Verdana; text-align: justify; background-color:F31212; color:white " >
    Las elecciones no se celebran exclusivamente en sistemas políticos democráticos.
  </p>
    </div>
  <div class="col-md-6 text-center">
      <p><font size=6 style = "font-family:Verdana; background-color:FFF702 ">¿ES UNA OBLIGACIÓN?</font></p> 
        <p style = "font-family:Verdana; text-align: justify; background-color:212EF9; color:white" >
El sufragio obligatorio (también, voto obligatorio) es un mecanismo electoral que considera al sufragio como un derecho y una obligación ciudadana, estableciendo el deber de concurrir a los centros de votación para ejercer ese derecho y obligación.
</p>
<p style = "font-family:Verdana; text-align: justify; background-color:F31212; color:white" >
Se diferencia del sufragio voluntario, que considera al sufragio solo como un derecho.
</p>
